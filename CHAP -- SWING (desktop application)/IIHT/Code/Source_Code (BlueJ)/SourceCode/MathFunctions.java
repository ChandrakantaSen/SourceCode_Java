class MathFunctions
{
    static void main(String args[])
    {
        double num1 = 6.25;
        double num2 = 8.75;

        System.out.println("Square Root: " + Math.sqrt(num1));
        System.out.println("Minimum Value: " + Math.min(num1,num2));
        System.out.println("Maximum Value: " + Math.max(num1,num2));
        System.out.println("Power Value: " + Math.pow(2,3));
        System.out.println("Minimum Value: " + Math.min(num1,num2));
    }
}