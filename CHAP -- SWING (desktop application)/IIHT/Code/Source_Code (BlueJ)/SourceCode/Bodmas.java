class Bodmas
{
    static void main(int a, int b)
    {
        int sum  = (a + b);
        int diff = (a - b);
        int mul  = (a * b);

        System.out.println("Sum is: " + sum);
        System.out.println("Difference is: " + diff);
        System.out.println("Multiplication is: " + mul);
    }
}