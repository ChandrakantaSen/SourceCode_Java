CREATE TABLE ITEM_MASTER(
ITEM_CODE	VARCHAR2(10) 	PRIMARY KEY,
ITEM_NAME	VARCHAR2(20)	NOT NULL,
RATE		NUMBER(7,2)	DEFAULT 0,
QUANTITY	NUMBER(10)	DEFAULT 0,
RE_ORDER	NUMBER(10))
/
CREATE TABLE ITEM_TRANS(
TR_NO		VARCHAR2(10)	PRIMARY KEY,
ITEM_CODE	VARCHAR2(10)	REFERENCES ITEM_MASTER(ITEM_CODE),
TR_QUANTITY	NUMBER(10)	CONSTRAINT VALID_QTY CHECK(TR_QUANTITY>0),
RATE		NUMBER(7,2),
OP_MODE		VARCHAR2(1),	CONSTRAINT VALID_OP CHECK(OP_MODE IN('I','R')),
REMARKS		VARCHAR2(20))
/
