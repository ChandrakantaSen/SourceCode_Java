public class triangular_number
{
    public static void main(int n)
    {
        int k=0,i,c=2,a=0;
        while(a<n)
        {
            c=c+1;
            for(i=1;i<c;i++)
            {
           
            k=k+i;
        }
        System.out.println(k);
        a=k;
        k=0;
        
    }
}
}
    
    
            
            
        
    