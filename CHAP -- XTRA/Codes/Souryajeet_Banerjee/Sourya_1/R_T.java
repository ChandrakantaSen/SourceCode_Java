import java.io.*;
public class R_T
{
    void rhombus(double d1,double d2)
    {
        double rh=1/2*d1*d2;
        System.out.println("area of Rhombus"+rh);
    }
    void trapezium(double a,double b,double h)
    {
        double tr=1/2*(a+b)*h;
        System.out.println("area of Trapezium"+tr);
    }
}