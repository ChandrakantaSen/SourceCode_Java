import java.io.*;
public class CSA
{
    void circle(double r)
    {
        double c=3.14*r*r;
        System.out.print(" Area of circle= "+c);
    }
    void square(double s)
    {
        s=s*s;
        System.out.print(" Area of square= "+s);
    }
    void rectangle(double n,double m)
    {
        double s=n*m;
        System.out.print(" Area of rectangle= "+s);
    }
}