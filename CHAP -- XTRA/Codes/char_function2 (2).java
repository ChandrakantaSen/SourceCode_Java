class char_function2{
    void main(char p)
    {
        //char p = 'S';
        if(Character.isUpperCase(p)==true)
        {
            System.out.println(p+" is in upper case");
        }
        else if(Character.isLowerCase(p)==true)
        {
            System.out.println(p+" is in lower case");
        }
    }
}        