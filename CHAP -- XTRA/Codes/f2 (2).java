//calculating area and perimeter of acircle
class f2
{//opening class
    public static void main(double r)
    {//opening main function
        double pie=(22.0/7);
        double p=2*pie*r;
        double a=(pie*r*r);
        System.out.println("area is"+a);
        System.out.println("perimeter is"+p);
    }//close main function
}//close f2