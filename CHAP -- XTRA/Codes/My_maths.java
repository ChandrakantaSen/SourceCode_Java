public class My_maths
{
    public static void main(String args[] )
    {
        double m = 4;int h = 4;
        double n= 16;
        System.out.println("the output of different mathematical function are:");
        System.out.println("square root of 4= "+Math.sqrt(m));
        System.out.println("minimum value between 4 & 16="+Math.min(m,n));
        System.out.println("maximum value between 4 & 16="+Math.max(m,n));
        System.out.println("4 raised to the power 3 ="+Math.pow(h,3));
        System.out.println("Natural log of 4="+Math.log(m));
        System.out.println("absolute value of(4-16)="+(float)Math.abs(m-n));
        System.out.println("rounded value of 4.89 ="+Math.round(m));
        System.out.println("the floor value of 4.89="+Math.floor(m));
        System.out.println("the ceil value of 4.89 ="+Math.ceil(m));
        System.out.println("Truncated value of 4.89="+Math.rint(m));
        System.out.println("Exponent value of 4.89="+Math.exp(m));
    }
}
